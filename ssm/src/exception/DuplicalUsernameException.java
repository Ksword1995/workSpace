package exception;

public class DuplicalUsernameException extends Exception {

	public DuplicalUsernameException() {
		super();
	}

	public DuplicalUsernameException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicalUsernameException(String message) {
		super(message);
	}

	public DuplicalUsernameException(Throwable cause) {
		super(cause);
	}
	
	
	
	
}
